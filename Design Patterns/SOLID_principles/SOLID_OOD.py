# SOLID is a set of five object-oriented principles that can help us write more maintainable, flexible and scalable code
# based on well-designed, cleanly structured classes
# SOLID is an acronym that groups five core principles that apply to object-oriented design.
# These principles are the following:
# 1) Single-responsibility principle(SRP)
# 2) Open-closed principle(OCP)
# 3) Liskov-substitution principle(LSP)
# 4) Interface segregation principle (ISP)
# 5) Dependency inversion principle (DIP)

