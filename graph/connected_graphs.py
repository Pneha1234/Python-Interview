# A connected graph is a graph where there is a path between any two nodes
# a broken graph is a graph where there is no path between any two nodes
# a component is a maximal connected subgraph of a graph
# example:
# 0 1 2
# 3 4 5
# 6 7 8
# 0, 1, 2 are connected to each other
# 3, 4, 5 are connected to each other
# 6, 7, 8 are connected to each other
# 0, 1, 2, 3, 4, 5, 6, 7, 8 are connected to each other
# 0, 1, 2, 3, 4, 5, 6, 7, 8 is a connected graph
# 0, 1, 2, 3, 4, 5, 6, 7, 8 is a component
# 0, 1, 2, 3, 4, 5, 6, 7, 8 is a connected graph    
